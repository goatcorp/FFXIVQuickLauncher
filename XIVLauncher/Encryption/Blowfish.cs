        private void BlowfishEncipher(ref UInt32 xl, ref UInt32 xr)
        {
            UInt32 temp;
            Int32 i;

            for (i = 0; i < N; ++i)
            {
                xl = xl ^ P[i];
                xr = F(xl) ^ xr;

                temp = xl;
                xl = xr;
                xr = temp;
            }

            temp = xl;
            xl = xr;
            xr = temp;

            xr = xr ^ P[N];
            xl = xl ^ P[N + 1];

        }

        private void BlowfishDecipher(ref UInt32 xl, ref UInt32 xr)
        {
            UInt32 temp;
            Int32 i;

            for (i = N + 1; i > 1; --i)
            {
                xl = xl ^ P[i];
                xr = F(xl) ^ xr;

                /* ExChange xl and xr */
                temp = xl;
                xl = xr;
                xr = temp;
            }

            /* ExChange xl and xr */
            temp = xl;
            xl = xr;
            xr = temp;

            xr = xr ^ P[1];
            xl = xl ^ P[0];

        }

